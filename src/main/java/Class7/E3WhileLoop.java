package Class7;

public class E3WhileLoop {
    public static void main(String[] args) {


        // we want to print  "Hello World" five
        // times on the console
            int counter=1;
            // this condition will return true 5 times
        while(counter<=5){
            System.out.println("Hello World");
            // in every iteration the value of the variable will
            //increase
            counter++;

        }









    }
}
