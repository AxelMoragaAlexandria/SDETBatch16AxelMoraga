package Homework;

public class Class7HW3 {
    public static void main(String[] args) {

// Print even numbers from 20 to 1 (2 ways) While Loop:For Loop

        int i=20;

        while(i>=1){
            System.out.print(i+" ");
            i-=2;
        }



        for(int j=20;j>=1;j-=2){
            System.out.print(j+" ");
        }
    }
}
