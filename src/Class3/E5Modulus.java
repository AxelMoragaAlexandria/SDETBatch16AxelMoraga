package Class3;

public class E5Modulus {
    public static void main(String[] args) {

        int number1=11;
        int number2=4;
        System.out.println(number1/number2);
        System.out.println(number1%number2);

        int noOfSlice=5;
        int noOfIndividuals=3;
        System.out.println(noOfSlice/noOfIndividuals);
        System.out.println(noOfSlice%noOfIndividuals);
    }
}
